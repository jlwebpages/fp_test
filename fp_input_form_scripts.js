   d.writeln('function generate_home_team_picks(document)');
   d.writeln('{');
   d.writeln('   var exit_loop = false;');
   d.writeln('');
   d.writeln('');
   d.writeln('   // Make sure the user name has been selected.');
   d.writeln('');
   d.writeln('   if (validate_name(document,false) == false)');
   d.writeln('   {');
   d.writeln('      alert("You must select your name before home team picks can be generated.");');
   d.writeln('');
   d.writeln('      return false;');
   d.writeln('   }');
   d.writeln('');
   d.writeln('   // Reset the picks_from_odds_button_pressed and random_picks_button_pressed flags and set the home_team_picks_button_pressed flag.');
   d.writeln('');
   d.writeln('   home_team_picks_button_pressed = true;');
   d.writeln('   picks_from_odds_button_pressed = false;');
   d.writeln('   random_picks_button_pressed    = false;');
   d.writeln('');
   d.writeln('   // Clear out the picks and spread_values arrays and also the picks and spreads on the Input Form.');
   d.writeln('');
   d.writeln('   clear_picks_from_input_form();');
   d.writeln('');
   d.writeln('   // Loop through this week\'s games and pick the home team.');
   d.writeln('');
   d.writeln('   for (var i = 0; i < '+number_of_ps_games+'; i++)');
   d.writeln('   {');
   d.writeln('      exit_loop = false;');
   d.writeln('');
   d.writeln('      // Loop through nfl_odds_array to get the spread_value for the current game and the total_points for the last game.');
   d.writeln('');
   d.writeln('      for (var j = 0; j < '+number_of_ps_games+'; j++)');
   d.writeln('      {');
   d.writeln('         // Initialize pick, spread_value, and total_points for the current game.');
   d.writeln('');
   d.writeln('         picks[i]         = "H";');
   d.writeln('         spread_values[i] = Math.floor(Math.random() * ('+random_picks_max_spread+') + 1);');
   d.writeln('         total_points     = Math.floor(Math.random() * (99) + 1);');
   d.writeln('');
   d.writeln('         // If NFL odds exist, override the initialized spread_value.');
   d.writeln('');
   d.writeln('         if (nfl_odds_array[0].length > 0)');
   d.writeln('         {');
   d.writeln('            if (nfl_odds_array[j][noa_team_index] == home_teams[i])');
   d.writeln('            {');
   d.writeln('               // The home team is favored so use the spread_value from the NFL odds.');
   d.writeln('');
   d.writeln('               spread_values[i] = nfl_odds_array[j][noa_spread_index];');
   d.writeln('');
   d.writeln('               exit_loop = true;');
   d.writeln('            }');
   d.writeln('            else if (nfl_odds_array[j][noa_team_index] == visiting_teams[i])');
   d.writeln('            {');
   d.writeln('               // The visiting team is favored so use a minimal spread_value of 1 since the home team is the pick.');
   d.writeln('');
   d.writeln('               spread_values[i] = 1;');
   d.writeln('');
   d.writeln('               exit_loop = true;');
   d.writeln('            }');
   d.writeln('         }');
   d.writeln('         else');
   d.writeln('         {');
   d.writeln('            // NFL odds don\'t exist, so use the initialized spread_value for this game.');
   d.writeln('')
   d.writeln('            exit_loop = true;');
   d.writeln('         }');
   d.writeln('');
   d.writeln('         if (exit_loop == true)');
   d.writeln('         {');
   d.writeln('            if (spread_values[i] == 0)');
   d.writeln('            {');
   d.writeln('               // The odds are even because the point spread is zero so set the point spread to 1.');
   d.writeln('');
   d.writeln('               spread_values[i] = 1;');
   d.writeln('            }');
   d.writeln('            else');
   d.writeln('            {');
   d.writeln('               // The spread_value is valid, so make sure it\'s a positive number.');
   d.writeln('');
   d.writeln('               spread_values[i] = Math.abs(spread_values[i]);');
   d.writeln('');
   d.writeln('               // If the spread_value is not a whole number, round it up or down randomly.');
   d.writeln('');
